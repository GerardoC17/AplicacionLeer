    private void jbtObtenerInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtObtenerInfoActionPerformed
        // TODO add your handling code here:
        ObtenerInfo o = new ObtenerInfo();
        JFileChooser ch = new JFileChooser();
        ch.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
        int returnVal = ch.showOpenDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            txtCampoDireccion.setText(ch.getSelectedFile().getAbsolutePath());
            ObtenerInfo oI = new ObtenerInfo();
            for (Tipos obj : oI.obtenerInfo(ch.getSelectedFile().getAbsolutePath())) {
                registro[0] = obj.getNombre();
                registro[1] = obj.getTamano();
                registro[2] = obj.getTipo();
                registro[3] = obj.getLineasCodigo();
                modelo.addRow(registro);
            } 
        }
        

    }//GEN-LAST:event_jbtObtenerInfoActionPerformed


